def _build_region_lookup(regions: dict[str, list[str]]) -> dict[str, str]:
    lookup: dict[str, str] = {}
    for province, labels in regions.items():
        province_key = province.lower()
        for label in labels:
            key = label.lower()
            existing = lookup.get(key)
            if existing is not None and existing != province_key:
                raise ValueError(
                    f"Duplicate region label '{label}' for provinces "
                    f"'{existing}' and '{province_key}'."
                )
            lookup[key] = province_key
    return lookup


